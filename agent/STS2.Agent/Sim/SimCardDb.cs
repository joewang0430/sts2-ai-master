    /// <summary>
    /// Returns true if <paramref name="cardType"/> is a known CardModel subclass.
    /// Use in FromReal where unknown cards should be silently skipped (mod resilience).
    /// </summary>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static bool TryGetId(Type cardType, out ushort id)
        => _byType.TryGetValue(cardType, out id);

    /// <summary>
    /// Returns the id, or throws if <paramref name="cardType"/> is unknown.
    /// Use where a missing entry means a build-time bug, not game-data drift.
    /// </summary>
    public static ushort GetId(Type cardType)
        => _byType.TryGetValue(cardType, out ushort id)
            ? id
            : throw new InvalidOperationException(
                $"SimCardDb: unknown CardModel subclass '{cardType.FullName}'. " +
                "Add it to SimCardId and SimCardDb.");
